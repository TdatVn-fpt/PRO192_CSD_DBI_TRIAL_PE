
package q3demo2;

public class Plate {
    private String region;
    private int code;

    public Plate() {
    }

    public Plate(String region, int code) {
        this.region = region;
        this.code = code;
    }

    public String getRegion() {
        return region;
    }

    public void setRegion(String region) {
        this.region = region;
    }

    public int getCode() {
        return code;
    }

    public void setCode(int code) {
        this.code = code;
    }
    
    
}
